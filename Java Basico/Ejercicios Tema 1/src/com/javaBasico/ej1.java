package com.javaBasico;


public class ej1 {
    public static void main(String[] args) throws Exception {
        
        // Creamos las variables y les asignamos un valor
        byte numeroByte = 1;
        short numeroShort = 15;
        int numeroInt = 200;
        long numeroLong = 200000;
        float numeroFloat = 20.2f;
        double numeroDouble = 21.4d;
        boolean booleano = true;
        char character = 'a';
        String texto = "Esto es un texto";


        // Imprimimos en la consola el valor de las variables
        System.out.println(numeroByte);
        System.out.println(numeroShort);
        System.out.println(numeroInt);
        System.out.println(numeroLong);
        System.out.println(numeroFloat);
        System.out.println(numeroDouble);
        System.out.println(booleano);
        System.out.println(character);
        System.out.println(texto);

    }
}
